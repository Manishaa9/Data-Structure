private void MyAppointments() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // clear existing rows

    for (Appointment appt : appointments) {
        model.addRow(new Object[]{appt.date, appt.time, appt.status});
    }
}


private void loadCancelAppointments() {
    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    model.setRowCount(0); // clear table

    LocalDate today = LocalDate.now();
    DateTimeFormatter formatter = DateTimeFormatter.ofPattern("d MMM yyyy");

    for (Appointment appt : appointments) {
        LocalDate apptDate = LocalDate.parse(appt.date, formatter);
        if (appt.status.equals("Booked") && apptDate.isAfter(today)) {
            model.addRow(new Object[]{appt.doctor, appt.date, appt.time, appt.status});
        }
    }
}
